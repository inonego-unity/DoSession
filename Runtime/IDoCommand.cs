using System;

namespace inonego.DoSession
{

   // ============================================================
   /// <summary>
   /// <br/> 실행/되돌리기 계약을 정의하는 Command 인터페이스.
   /// <br/> 모든 Undo/Redo 가능한 작업은 이 인터페이스를 구현한다.
   /// </summary>
   // ============================================================
   public interface IDoCommand
   {

      // ------------------------------------------------------------
      /// <summary>
      /// 작업을 실행한다.
      /// </summary>
      // ------------------------------------------------------------
      void Do();

      // ------------------------------------------------------------
      /// <summary>
      /// 작업을 되돌린다. Do()의 역연산.
      /// </summary>
      // ------------------------------------------------------------
      void Undo();

      // ------------------------------------------------------------
      /// <summary>
      /// 현재 이 Command가 되돌리기 가능한지 여부.
      /// </summary>
      // ------------------------------------------------------------
      bool CanUndo { get; }

      // ------------------------------------------------------------
      /// <summary>
      /// 사람이 읽을 수 있는 작업 설명.
      /// </summary>
      // ------------------------------------------------------------
      string Desc { get; }

   }

}
