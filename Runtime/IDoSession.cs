using System;

namespace inonego.DoSession
{

   // ============================================================
   /// <summary>
   /// <br/> DoSession의 핵심 API만 노출하는 인터페이스.
   /// <br/> DI, 테스트, 모킹 용도.
   /// </summary>
   // ============================================================
   public interface IDoSession
   {

      // ------------------------------------------------------------
      /// <summary>
      /// Command를 실행하고 Undo 스택에 추가한다.
      /// </summary>
      // ------------------------------------------------------------
      void Do(IDoCommand command);

      // ------------------------------------------------------------
      /// <summary>
      /// 마지막 작업을 되돌린다. 성공 시 true.
      /// </summary>
      // ------------------------------------------------------------
      bool Undo();

      // ------------------------------------------------------------
      /// <summary>
      /// 마지막으로 되돌린 작업을 다시 실행한다. 성공 시 true.
      /// </summary>
      // ------------------------------------------------------------
      bool Redo();

      // ------------------------------------------------------------
      /// <summary>
      /// Undo 가능 여부.
      /// </summary>
      // ------------------------------------------------------------
      bool CanUndo { get; }

      // ------------------------------------------------------------
      /// <summary>
      /// Redo 가능 여부.
      /// </summary>
      // ------------------------------------------------------------
      bool CanRedo { get; }

      // ------------------------------------------------------------
      /// <summary>
      /// 히스토리 변경 시 발생하는 이벤트.
      /// </summary>
      // ------------------------------------------------------------
      event Action OnChange;

   }

}
